"""
This module contains basic mathematical operations.
"""

def add(a: float, b: float) -> float:
    """
    Returns the sum of two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    return a + b

def multiply(a: float, b: float) -> float:
    """
    Returns the product of two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The product of a and b.
    """
    return a * b
